package com.kajiwara.omnichest.slotlock;

/**
 * 1 つのロックエントリが「何を」守るかのモード。
 *
 * <p>
 * <ul>
 * <li>{@link #SLOT} — <b>スロット固定</b>: スロット番号そのものを保護する。
 *     スロット内容 (アイテム種別) が何であっても、そのスロットへ/から自動移動を起こさない。
 *     一般的な「Hotbar 1 番は常に剣置き場」のような用途。</li>
 * <li>{@link #ITEM} — <b>アイテム固定</b>: スロットでは無く特定の ItemStack 識別子を追跡する。
 *     プレイヤーが手動でアイテムを別スロットへ動かした場合、ロックも追随する
 *     (= 次の整理時には新スロットを守る)。
 *     初期参照スロットだけ持ち、毎フレーム探索はしない (= イベント駆動 / オンデマンド)。</li>
 * </ul>
 *
 * <p>
 * モードは 1 エントリごとに決まる。同じインベントリ内で SLOT と ITEM を混在させても良い。
 */
public enum SlotLockMode {
    SLOT,
    ITEM
}
